#include "libft.h"

int	arrdel(char ***array)
{
	char	**arr;

	arr = *array;
	if (arr)
	{
		while (*arr)
		{
			free(*arr);
			*arr = NULL;
			arr++;
		}
	}
	arr = NULL;
	return (1);
}
